s={10,10,11,12,13,14}
print(s)
print()

# add() function---add function will add the value at the end in set
s.add(15)
print(s)

# pop() function---pop function will remove the element of first index
print()
s.pop()
print(s)

# remove() function--- remove function will remove the element of given
print()
s.remove(13)
print(s)

# discard() function-- discard function will remove the element of given 
#                       but if but if element is not found it will not throw an error
l={"ram","shyam","radha","bhola"}
l.discard("ram")
print(l)


# update function()-- update function will add more than one element in set
p=["kajal","radha","meera"]
l.update(p)
print(l)


